public class OperadoresAritmeticos {
    public static void main(String[] args) {
        System.out.println("Operadores aritmeticos");
        int a = 4,  b = 2, resultado;
        //suma +
        resultado = a + b;
        System.out.println("resultado + = " + resultado);
        //resta -
        resultado = a - b;
        System.out.println("resultado - = " + resultado);
        //multiplicacion *
        resultado = a * b;
        System.out.println("resultado * = " + resultado);
        //division /
        resultado = a / b;
        System.out.println("resultado / = " + resultado);
        //modulo %
        resultado = a % b;
        System.out.println("resultado % = " + resultado);
    }
}
